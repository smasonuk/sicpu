// Math Unit MMIO
int* M_A    = 0xFF20;
int* M_B    = 0xFF21;
int* M_RES  = 0xFF22;
int* M_CTRL = 0xFF23;

// Q8.8 Multiply wrapper
int f_mul(int a, int b) {
    *M_CTRL = 0; 
    *M_A = a;
    *M_B = b;
    return *M_RES;
}

